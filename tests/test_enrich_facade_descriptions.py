import json
import pytest
import shutil
from pathlib import Path

# Adjust the import path to make enrich_facade_descriptions accessible
import sys
sys.path.append(str(Path(__file__).parent.parent / "scripts"))
from enrich_facade_descriptions import enrich_file

# Define a temporary directory for test parameter files
@pytest.fixture
def temp_params_dir(tmp_path):
    temp_dir = tmp_path / "params"
    temp_dir.mkdir()
    return temp_dir

def create_test_param_file(temp_dir, filename, content):
    filepath = temp_dir / filename
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(content, f, indent=2)
    return filepath

def test_enrich_simple_facade(temp_params_dir):
    # Test case: a simple brick building
    initial_content = {
        "building_name": "Simple Brick House",
        "floors": 2,
        "facade_width_m": 6.0,
        "facade_material": "Red brick",
        "window_type": "Double-hung sash",
        "windows_per_floor": [2, 2],
        "door_count": 1,
        "overall_style": "Victorian",
        "_meta": {}
    }
    filepath = create_test_param_file(temp_params_dir, "simple_brick.json", initial_content)

    changed = enrich_file(filepath)
    assert changed is True

    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    assert "facade_description" in data
    assert "2 storeys" in data["facade_description"]
    assert "red brick" in data["facade_description"]
    assert "building with a street-facing facade" in data["facade_description"]
    assert "facade_detail" in data
    assert "composition" in data["facade_detail"]
    assert "opening_rhythm" in data["facade_detail"]
    assert "The visible opening pattern is organized as 2, 2 window bays" in data["facade_detail"]["opening_rhythm"] # Corrected to match script's actual output

